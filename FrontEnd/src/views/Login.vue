<template>
  <div class="flex justify-center my-20">
    <Form @submit="handleLogin" :validation-schema="schema" class="flex flex-col w-1/2 lg:w-1/4">
      <label for="user" class="py-2">Usuario</label>
      <Field type="text" name="username" id="user" class="inputCustom" required/>
      <ErrorMessage name="username" class="error-feedback" />

      <label for="pass" class="py-2 mt-4">Contraseña</label>
      <Field type="password" name="password" id="pass" class="inputCustom" required/>
      <ErrorMessage name="password" class="error-feedback" />


      <button type="submit" class="mt-6 py-2 px-4 bg-ligth_orange hover:bg-primary focus:ring-offset-accent text-white w-full transition ease-in duration-200 text-center text-base font-semibold shadow-md focus:outline-none focus:ring-2 focus:ring-offset-2 rounded-lg"> Login
      </button>
      <div v-if="message" class="alert alert-danger" role="alert">
        {{ message }}
      </div>
    </Form>
  </div>

</template>

<script>
import { Form, Field, ErrorMessage } from "vee-validate";
import * as yup from "yup";

export default {
  name: "Login",
  components: {
    Form,
    Field,
    ErrorMessage,
  },
  data() {
    const schema = yup.object().shape({
      username: yup.string().required("El usuario es obligatorio!"),
      password: yup.string().required("La contraseña es obligatoria!"),
    });

    return {
      loading: false,
      message: "",
      schema,
    };
  },
  computed: {
    loggedIn() {
      return this.$store.state.auth.status.loggedIn;
    },
  },
  created() {
    if (this.loggedIn) {
      this.$router.push("/profile");
    }
  },
  methods: {
    handleLogin(user) {
      this.loading = true;

      this.$store.dispatch("auth/login", user).then(
          () => {
            this.$router.push("/profile");
          },
          (error) => {
            this.loading = false;
            this.message =
                (error.response &&
                    error.response.data &&
                    error.response.data.message) ||
                error.message ||
                error.toString();
          }
      );
    },
  },
};
</script>

